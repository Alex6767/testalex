
public class Event {

	private String title;

	public Event(String title) {
		super();
		this.title = title;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	@Override
	public String toString() {
		return "Event [title=" + title + "]";
	}
	
	
	
	
	
	
	
	
	
	
} // Fin de la classe

